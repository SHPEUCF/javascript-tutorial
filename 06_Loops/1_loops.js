/*
1. LOOPS

 Repeat a task multiple times.

 Loops let us tell the computer to loop over
 a block of code so that we don't have
 to write out the same process over and over.

 There are two kinds of loops we
 will learn in this lesson:

   - for loops, which let us loop a
   block of code a known amount of times.

   - while loops, which let us loop a block
   of code an unknown amount of times.


*/

let cookies = ['chocolate chip', 'raisin', 'macadamia nut', 'sugar'];

for (let i = 0; i<cookies.length; i++) {
  console.log('I would love to eat a ' + cookies[i] + ' cookie right now!');
}


