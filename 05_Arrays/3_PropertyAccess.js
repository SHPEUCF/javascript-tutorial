/*
3. PROPERTY ACCESS

Each item in an array has a numbered position.
We can access individual items using their
numbers, just like we would in an
ordinary list.

Remember to start counting from 0 NOT 1

*/

// select items like this

let newYearsResolutions = ['Rappel into a cave', 'Take a falconry class', 'Learn to juggle'];

console.log(newYearsResolutions[0]);
// Output: 'Rappel into a cave'

// Can also use this method w/strings
let hello = 'Hello World';
console.log(hello[6]);
// Output: W



